package com.OwnerActivity;

public class FullDetails {
	private String vehicle_no;
	private String oil_serv;
	private String wat_wash;
	private String gen_check;
	private String total;
	private String email;
	private String phone;
	private String state;
	private String book_date;
	public String getVehicle_no() {
		return vehicle_no;
	}
	public void setVehicle_no(String vehicle_no) {
		this.vehicle_no = vehicle_no;
	}
	public String getOil_serv() {
		return oil_serv;
	}
	public void setOil_serv(String oil_serv) {
		this.oil_serv = oil_serv;
	}
	public String getWat_wash() {
		return wat_wash;
	}
	public void setWat_wash(String wat_wash) {
		this.wat_wash = wat_wash;
	}
	public String getGen_check() {
		return gen_check;
	}
	public void setGen_check(String gen_check) {
		this.gen_check = gen_check;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getBook_date() {
		return book_date;
	}
	public void setBook_date(String book_date) {
		this.book_date = book_date;
	}
	
}
